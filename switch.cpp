#include<iostream>

using namespace std;
int main(){
	char c;
	cin>>c;
	switch(c){
		case 'A':
			cout<<"Hello"<<endl;
			break;
		case 'B':
			cout<<"Namaste"<<endl;
			break;
		case 'C':
			cout<<"Halo"<<endl;
		break;
		case 'D':
			cout<<"xyz"<<endl;
	break;
		case 'E':
			cout<<"abc"<<endl;
			break;
			default:
				cout<<"Invalid"<<endl;
				break;
			
	}
}
